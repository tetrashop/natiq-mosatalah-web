## 🔬 روش‌شناسی و الگوریتم‌ها

### ۱. الگوریتم استخراج خودکار عبارات (Auto-Extraction)

هسته AI از روش **Pattern Matching مبتنی بر Regex** برای شناسایی عبارات در اسناد HTML استفاده می‌کند:

```

الگوی تشخیص:
"([^"]+)":\s\{[^}]"mani":\s"([^"])"[^}]"mesal":\s"([^"]*)"

```

**مراحل پردازش**:
1. اسکن فایل‌های HTML پروژه
2. شناسایی الگوهای JSON embedded
3. استخراج عبارت، معنی و مثال
4. فیلتر کردن کلیدهای متادیتا (دسته‌بندی‌ها)
5. حذف عبارات تکراری
6. ذخیره در پایگاه داده JSON

### ۲. سیستم یادگیری تطبیقی (Adaptive Learning)

هسته AI با تحلیل منابع سیستم، پارامترهای عملکردی خود را تنظیم می‌کند:

| سطح سیستم | RAM | CPU Cores | فاصله یادگیری | حجم کش |
|-----------|-----|-----------|---------------|--------|
| **HIGH** | ≥۸GB | ≥۴ | ۱۸۰ ثانیه | ۱۰۰۰ |
| **MEDIUM** | ≥۴GB | ≥۲ | ۳۰۰ ثانیه | ۵۰۰ |
| **LOW** | <۴GB | ۱ | ۶۰۰ ثانیه | ۱۰۰ |

### ۳. بهینه‌سازی خودکار پایگاه داده

- **حذف تکراری‌ها**: با استفاده از JSON deduplication
- **فشرده‌سازی**: gzip برای سیستم‌های با فضای محدود
- **ایندکس‌گذاری مجدد**: پس از هر ۵۰ چرخه یادگیری
- **پشتیبان‌گیری چرخشی**: نگهداری ۳-۱۰ نسخه بر اساس ظرفیت

