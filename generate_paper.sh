### چالش‌های فنی و راه‌حل‌ها

#### چالش ۱: ناسازگاری دستور `df` در Termux
**مشکل**: دستور `df -BG` در محیط Termux پشتیبانی نمی‌شد
**راه‌حل**: جایگزینی با `df -m` و تبدیل به GB
```bash
# قبل (خطا):
DISK_TOTAL=$(df -BG / | awk 'NR==2 {print $2}')

# بعد (رفع شده):
DISK_TOTAL=$(df -m / | awk 'NR==2 {print $2}')
DISK_TOTAL_GB=$((DISK_TOTAL / 1024))
```

چالش ۲: خطای Division by Zero

مشکل: در محاسبه درصد فضای دیسک، مقسوم‌علیه صفر می‌شد
راه‌حل: افزودن مقادیر پیش‌فرض

```bash
DISK_TOTAL=${DISK_TOTAL:-10000}
DISK_AVAILABLE=${DISK_AVAILABLE:-5000}
```

چالش ۳: JSON ناقص در استخراج عبارات

مشکل: استخراج عبارات از HTML، JSON ناقص تولید می‌کرد
راه‌حل: بازنویسی الگوریتم استخراج با Python و regex دقیق‌تر

```python
pattern = r'"([^"]+)":\s*\{[^}]*"mani":\s*"([^"]*)"[^}]*"mesal":\s*"([^"]*)"'
matches = re.findall(pattern, content)
# فیلتر کردن کلیدهای متادیتا
categories = ['estelahat', 'zarbolmasalha', 'ashare_mardomi']
phrases = {m[0]: {"mani": m[1], "mesal": m[2]} 
           for m in matches if m[0] not in categories}
```

چالش ۴: خطای Here-Document در Bash

مشکل: استفاده از << برای متون چندخطی باعث خطای syntax می‌شد
راه‌حل: جایگزینی با echo برای فایل‌های ساده و cat > file << 'EOF' با کوتیشن

